// Custom service worker with aggressive cache clearing
// This runs before the Workbox precaching to ensure old caches are cleared

import { clientsClaim, skipWaiting } from "workbox-core";
import {
  cleanupOutdatedCaches,
  precacheAndRoute,
  type PrecacheEntry,
} from "workbox-precaching";

type ServiceWorkerLikeGlobal = {
  __WB_MANIFEST: Array<string | PrecacheEntry>;
  addEventListener: (
    type: string,
    listener: (event: {
      waitUntil: (promise: Promise<unknown>) => void;
      data?: { type?: string };
    }) => void,
  ) => void;
  clients: {
    matchAll: () => Promise<Array<{ postMessage: (message: { type: string }) => void }>>;
  };
  skipWaiting: () => void;
};

// Immediately take control and skip waiting
skipWaiting();
clientsClaim();

// Clean up any outdated caches from previous builds
cleanupOutdatedCaches();

// Precache all assets generated by the build
// This will be replaced by the actual manifest at build time
const swGlobal = globalThis as unknown as {
  __WB_MANIFEST: Array<string | PrecacheEntry>;
};
const typedSwGlobal = swGlobal as unknown as ServiceWorkerLikeGlobal;
precacheAndRoute(typedSwGlobal.__WB_MANIFEST);

// Log activation for debugging
typedSwGlobal.addEventListener("activate", (event) => {
  console.log("[SW] Service Worker activated - clearing old caches");

  event.waitUntil(
    caches
      .keys()
      .then((cacheNames) => {
        return Promise.all(
          cacheNames.map((cacheName) => {
            // Delete caches that aren't part of our current precache
            if (!cacheName.includes("workbox-precache")) {
              console.log(`[SW] Deleting old cache: ${cacheName}`);
              return caches.delete(cacheName);
            }
          }),
        );
      })
      .then(() => {
        console.log("[SW] Old caches cleared successfully");
        // Tell all clients to reload
        return typedSwGlobal.clients.matchAll().then((clients) => {
          for (const client of clients) {
            client.postMessage({ type: "SW_ACTIVATED" });
          }
        });
      }),
  );
});

// Handle messages from the main thread
typedSwGlobal.addEventListener("message", (event) => {
  if (event.data && event.data.type === "SKIP_WAITING") {
    typedSwGlobal.skipWaiting();
  }
});

