import { memo } from "react";

// Base icon type with common props
interface IconProps {
  className?: string;
  size?: "sm" | "md" | "lg";
}

// Size mappings for consistent icon sizing
const sizeClasses = {
  sm: "w-4 h-4",
  md: "w-5 h-5",
  lg: "w-6 h-6",
};

// HOC to apply consistent styling to all icons
function createIcon(SvgComponent: React.FC<IconProps>) {
  return memo(function Icon({
    className = "",
    size = "md",
    ...props
  }: IconProps) {
    const sizeClass = sizeClasses[size] || sizeClasses.md;
    return <SvgComponent className={`${sizeClass} ${className}`} {...props} />;
  });
}

